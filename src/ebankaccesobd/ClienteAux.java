public class ClienteAux {
    
    private String dni, nombre;

    public ClienteAux(String dni, String nombre) {
        this.dni = dni;
        this.nombre = nombre;
    }

    public String getDni() {
        return dni;
    }

    public String getNombre() {
        return nombre;
    }
    
    
}
